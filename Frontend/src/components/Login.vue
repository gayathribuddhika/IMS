<template>
  <div id="form">
    <h2>
      <center>Admin Login</center>
    </h2>

    <b-container fluid>
      <div>
        <center>
          <b-card
            bg-variant
            text-variant="dark"
            border-variant="dark"
            style="max-width: 40rem;"
            align="left"
          >
            <b-form @submit.prevent="login">
              <center>
                <p id="msg" v-if="submit_login === 'error'">{{ err_msg }}</p>
              </center>
              <b-form-group label="Username:" label-for="username">
                <b-form-input
                  id="username"
                  placeholder="Enter your username"
                  required
                  v-model="login_form.username"
                />
              </b-form-group>

              <b-form-group label="Password:" label-for="password">
                <b-form-input
                  id="password"
                  type="password"
                  placeholder="Enter your password"
                  required
                  v-model="login_form.password"
                />
                <br />
                <router-link to="/login/resetpassword"
                  >Forgot your password?</router-link
                >
              </b-form-group>
              <b-button
                type="submit"
                variant="success"
                :disabled="submit_login === 'ok'"
                >Login</b-button
              >
            </b-form>
          </b-card>
        </center>
      </div>
    </b-container>
  </div>
</template>

<script>
import { required } from "vuelidate/lib/validators";
import axios from "axios";

export default {
  name: "Login",
  data() {
    return {
      login_form: {
        username: "",
        password: "",
        user_type: ""
      },
      users: [],

      submit_login: null,
      err_msg: "Invalid Username or Password"
    };
  },

  mounted() {
    axios
      .get("http://localhost:8085/login")
      .then(response => {
        this.users = response.data;
        console.log(this.users); //ok
      })
      .catch(err => {
        console.log(err);
      });
  },

  methods: {
    login() {
      let newLogin = {
        username: this.login_form.username,
        password: this.login_form.password
      };
      console.log(newLogin);

      axios
        .post("http://localhost:8085/login", newLogin)
        .then(resposne => {
          console.log(resposne);
          if ((this.submit_login = "ok")) {
            if (
              this.users[0].username == newLogin.username &&
              this.users[0].password == newLogin.password
            ) {
              this.$router.push({ name: "AdminDashboard" });
            }
            if (
              (this.users[1].username == newLogin.username &&
                this.users[1].password == newLogin.password) ||
              (this.users[2].username == newLogin.username &&
                this.users[2].password == newLogin.password)
            ) {
              this.$router.push({ name: "StaffDashboard" });
            }
          }
        })
        .catch(error => {
          console.log(error);
          console.log(this.err_msg);
          this.submit_login = "error";
        });
    }
  }
};
</script>

